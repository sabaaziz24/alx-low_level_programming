#include "main.h"

/**                                                                                                                                                                     
 * print_alphabet -  prints _putchar, followed by a new line                                                                                                                      
 * Return: void                                                                                                                                           
 */
void print_alphabet(void){
        char a;
        for (a = 'a'; a <= 'z'; a++){
                _putchar(a);
        }
}
